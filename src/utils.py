def to_categorical(y, num_classes=None, dtype='float32'):
    """
    Keras implementation to one-hot encode the target labels
    """
    y = np.array(y, dtype='int')
    input_shape = y.shape
    if input_shape and input_shape[-1] == 1 and len(input_shape) > 1:
        input_shape = tuple(input_shape[:-1])
    y = y.ravel()
    if not num_classes:
        num_classes = np.max(y) + 1
    n = y.shape[0]
    categorical = np.zeros((n, num_classes), dtype=dtype)
    categorical[np.arange(n), y] = 1
    output_shape = input_shape + (num_classes,)
    categorical = np.reshape(categorical, output_shape)
    return categorical


def weights_init(m):
    """
    Initialize weights for convolutional and batch normalization layers
    """
    classname = m.__class__.__name__
    if classname.find('Conv') != -1:
        m.weight.data.normal_(0.0, 0.02)
    elif classname.find('BatchNorm') != -1:
        m.weight.data.normal_(1.0, 0.02)
        m.bias.data.fill_(0)


def laplaceMechanism(x, epsilon, device):
    """
    Adds 0-mean laplace noise to the input x where
    standard deviation is monitored by argument epsilon
    """
    x += torch.Tensor(np.random.laplace(0, 1.0/epsilon, x.shape)).to(device)
    return x

